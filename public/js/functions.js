  function get_type(type)
  {
    switch(type)
    {
      case 1:
        return "Photo gallery";
      case 2:
        return "Frame";
      case 3:
        return "Text";
      case 4:
        return "Notification";
      case 5:
        return "Web page";
      case 21:
        return "Catalog";
      case 22:
        return "Page";
      case 23:
        return "App";
      case 24:
        return "User";
      case 25:
        return "Company";
      case 26:
        return "Offer";      
    }
  }
